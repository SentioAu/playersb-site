/* PlayersB shared client behaviour: theme toggle, analytics, mobile nav,
   search box, service-worker registration. Loaded with `defer`, so the DOM
   is ready by the time this runs. The early FOUC-prevention snippet for the
   theme is inlined in <head> separately. */
(function () {
  "use strict";

  var THEME_KEY = "playersb-theme";
  var root = document.documentElement;

  function track(eventName, params) {
    if (typeof window.playersbTrack === "function") {
      try {
        window.playersbTrack(eventName, params);
      } catch (_) { /* swallow */ }
    }
  }

  function setTheme(next) {
    if (next === "dark") {
      root.setAttribute("data-theme", "dark");
    } else {
      root.removeAttribute("data-theme");
    }
    try { localStorage.setItem(THEME_KEY, next); } catch (_) { /* private mode */ }
  }

  function initThemeToggle() {
    var button = document.getElementById("themeToggle");
    if (!button) return;
    function paint() {
      var isDark = root.getAttribute("data-theme") === "dark";
      button.textContent = isDark ? "Light mode" : "Dark mode";
      button.setAttribute("aria-pressed", String(isDark));
    }
    paint();
    button.addEventListener("click", function () {
      var next = root.getAttribute("data-theme") === "dark" ? "light" : "dark";
      setTheme(next);
      paint();
      track("theme_toggle", { theme: next });
    });
  }

  function initLinkTracking() {
    document.addEventListener("click", function (e) {
      var anchor = e.target && e.target.closest && e.target.closest("a[href]");
      if (!anchor) return;
      var href = anchor.getAttribute("href") || "";
      var text = (anchor.textContent || "").trim().slice(0, 80);
      var isOutbound = /^https?:\/\//i.test(href) && !href.includes(window.location.host);
      var isNav = anchor.classList.contains("nav-link");
      var isCta = anchor.classList.contains("button");
      if (isOutbound) return track("outbound_click", { destination: href, link_text: text });
      if (isNav) return track("nav_click", { destination: href, link_text: text });
      if (isCta) return track("cta_click", { destination: href, link_text: text });
    });
  }

  function initMobileNav() {
    var btn = document.getElementById("navToggle");
    var nav = document.querySelector(".site-nav");
    if (!btn || !nav) return;
    btn.addEventListener("click", function () {
      var open = nav.classList.toggle("is-open");
      btn.setAttribute("aria-expanded", String(open));
    });
    nav.addEventListener("click", function (e) {
      if (e.target.tagName === "A") {
        nav.classList.remove("is-open");
        btn.setAttribute("aria-expanded", "false");
      }
    });
  }

  // Lightweight, dependency-free site search. Loads /data/search-index.json
  // (generated by scripts/generate-search-index.mjs) and ranks against query
  // tokens with a small TF-style weighting. Good enough for ~500 entries.
  var searchIndexPromise = null;
  function loadSearchIndex() {
    if (!searchIndexPromise) {
      searchIndexPromise = fetch("/data/search-index.json", { cache: "force-cache" })
        .then(function (r) { return r.ok ? r.json() : { entries: [] }; })
        .catch(function () { return { entries: [] }; });
    }
    return searchIndexPromise;
  }

  function tokenize(s) {
    return String(s || "")
      .toLowerCase()
      .replace(/[^a-z0-9\s]/g, " ")
      .split(/\s+/)
      .filter(Boolean);
  }

  function scoreEntry(entry, qTokens) {
    var hayTokens = entry._t || (entry._t = tokenize(
      [entry.name, entry.team, entry.position, entry.section, entry.competition].filter(Boolean).join(" "),
    ));
    var score = 0;
    for (var i = 0; i < qTokens.length; i++) {
      var q = qTokens[i];
      var matched = false;
      for (var j = 0; j < hayTokens.length; j++) {
        var h = hayTokens[j];
        if (h === q) { score += 4; matched = true; }
        else if (h.startsWith(q)) { score += 2; matched = true; }
        else if (h.indexOf(q) !== -1) { score += 1; matched = true; }
      }
      if (!matched) return 0;
    }
    if (entry.section === "player") score += 1;
    return score;
  }

  function renderResults(box, results, query) {
    if (!results.length) {
      box.innerHTML = '<p class="search-empty">No matches for &ldquo;' +
        escapeHtml(query) + '&rdquo;.</p>';
      return;
    }
    var html = '<ul class="search-results" role="listbox">';
    for (var i = 0; i < results.length; i++) {
      var r = results[i];
      html += '<li role="option"><a href="' + escapeAttr(r.url) + '">' +
        '<span class="search-name">' + escapeHtml(r.name) + '</span>' +
        '<span class="search-meta">' + escapeHtml(r.section || "") +
        (r.team ? " — " + escapeHtml(r.team) : "") +
        '</span></a></li>';
    }
    html += "</ul>";
    box.innerHTML = html;
  }

  function escapeHtml(s) {
    return String(s).replace(/[&<>"]/g, function (c) {
      return { "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;" }[c];
    });
  }
  function escapeAttr(s) { return escapeHtml(s).replace(/'/g, "&#39;"); }

  function initSearch() {
    var input = document.getElementById("siteSearch");
    var box = document.getElementById("siteSearchResults");
    if (!input || !box) return;
    var debounceId = null;
    input.addEventListener("input", function () {
      var query = input.value.trim();
      clearTimeout(debounceId);
      if (!query) { box.innerHTML = ""; return; }
      debounceId = setTimeout(function () {
        loadSearchIndex().then(function (idx) {
          var qTokens = tokenize(query);
          if (!qTokens.length) { box.innerHTML = ""; return; }
          var entries = idx.entries || [];
          var scored = [];
          for (var i = 0; i < entries.length; i++) {
            var s = scoreEntry(entries[i], qTokens);
            if (s > 0) scored.push({ s: s, e: entries[i] });
          }
          scored.sort(function (a, b) { return b.s - a.s; });
          renderResults(box, scored.slice(0, 12).map(function (x) { return x.e; }), query);
          track("search_query", { query: query.slice(0, 80), result_count: scored.length });
        });
      }, 120);
    });

    // Allow ?q=... to pre-populate (matches the SearchAction in JSON-LD).
    var params = new URLSearchParams(window.location.search);
    var pre = params.get("q");
    if (pre) {
      input.value = pre;
      input.dispatchEvent(new Event("input"));
    }
  }

  // Watch-list: localStorage-backed favorites with shareable URL via the
  // ?watch= query param. Decoupled from the static-generation pipeline so it
  // works across every player page without re-deploys.
  var WATCH_KEY = "playersb-watch-v1";
  function readWatch() {
    try {
      var raw = localStorage.getItem(WATCH_KEY);
      if (!raw) return [];
      var parsed = JSON.parse(raw);
      return Array.isArray(parsed) ? parsed : [];
    } catch (_) { return []; }
  }
  function writeWatch(list) {
    try { localStorage.setItem(WATCH_KEY, JSON.stringify(list)); } catch (_) {}
  }
  function isWatched(list, id) {
    for (var i = 0; i < list.length; i++) if (list[i].id === id) return true;
    return false;
  }
  function paintWatchToggle(btn, watched) {
    btn.setAttribute("aria-pressed", String(watched));
    btn.textContent = watched ? "★ Saved" : "☆ Save to watch-list";
  }
  function initWatchToggles() {
    var list = readWatch();
    var buttons = document.querySelectorAll(".watch-toggle");
    buttons.forEach(function (btn) {
      var id = btn.getAttribute("data-watch-id");
      if (!id) return;
      paintWatchToggle(btn, isWatched(list, id));
      btn.addEventListener("click", function () {
        list = readWatch();
        var name = btn.getAttribute("data-watch-name") || id;
        if (isWatched(list, id)) {
          list = list.filter(function (x) { return x.id !== id; });
          track("watch_remove", { id: id });
        } else {
          list.push({ id: id, name: name, addedAt: Date.now() });
          track("watch_add", { id: id });
        }
        writeWatch(list);
        document.querySelectorAll('.watch-toggle[data-watch-id="' + id + '"]')
          .forEach(function (b) { paintWatchToggle(b, isWatched(list, id)); });
        renderWatchList();
      });
    });

    // Hydrate from ?watch=id1,id2 (shareable URL): merge into local list.
    try {
      var params = new URLSearchParams(window.location.search);
      var shared = params.get("watch");
      if (shared) {
        var ids = shared.split(",").map(function (s) { return s.trim(); }).filter(Boolean);
        var current = readWatch();
        var changed = false;
        for (var i = 0; i < ids.length; i++) {
          if (!isWatched(current, ids[i])) {
            current.push({ id: ids[i], name: ids[i], addedAt: Date.now() });
            changed = true;
          }
        }
        if (changed) {
          writeWatch(current);
          track("watch_import", { count: ids.length });
        }
      }
    } catch (_) {}
    renderWatchList();
  }
  function renderWatchList() {
    var box = document.getElementById("watchList");
    if (!box) return;
    var list = readWatch();
    if (!list.length) {
      box.innerHTML = '<p class="watch-list-empty">No saved players yet. Click ☆ on any profile to save.</p>';
      return;
    }
    var shareUrl = window.location.origin + "/players/?watch=" +
      list.map(function (x) { return encodeURIComponent(x.id); }).join(",");
    var html = '<div class="watch-list">';
    for (var i = 0; i < list.length; i++) {
      var x = list[i];
      html += '<a class="watch-list-item" href="/players/' + encodeURIComponent(x.id) + '/">★ ' +
        escapeHtml(x.name || x.id) + '</a>';
    }
    html += "</div>";
    html += '<p class="meta-text"><a href="' + escapeAttr(shareUrl) + '" data-track-event="watch_share">Share this watch-list</a></p>';
    box.innerHTML = html;
  }

  function initServiceWorker() {
    if (!("serviceWorker" in navigator)) return;
    if (window.location.protocol !== "https:" && window.location.hostname !== "localhost") return;
    window.addEventListener("load", function () {
      navigator.serviceWorker.register("/sw.js").catch(function () {
        /* offline support is best-effort */
      });
    });
  }

  function initEngagedRead() {
    setTimeout(function () {
      track("engaged_read", { engaged_seconds: 30 });
    }, 30000);
  }

  function ready(fn) {
    if (document.readyState !== "loading") fn();
    else document.addEventListener("DOMContentLoaded", fn);
  }

  ready(function () {
    initThemeToggle();
    initLinkTracking();
    initMobileNav();
    initSearch();
    initWatchToggles();
    initEngagedRead();
  });
  initServiceWorker();
})();
